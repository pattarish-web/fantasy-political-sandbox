def rewrite_opening() -> None:
    """Update the three chapter rows, regenerate exports, and remove the stray duplicate."""
    db.init_db()
    for round_num, chapter in CANONICAL_OPENING.items():
        db.replace_chapter(round_num, chapter["title"], chapter["body"], chapter["tone"])
    db.repair_image_prompt_descriptions()
    for round_num in CANONICAL_OPENING:
        export_chapter(db.get_chapter_by_round(round_num))
    export_all_characters()
    rebuild_index(db.list_chapters())
    (config.CHRONICLE_DIR / "chapter-012.html").unlink(missing_ok=True)


def main() -> None:
    rewrite_opening()
    print("Canonical opening rewritten and chronicle rebuilt.")


